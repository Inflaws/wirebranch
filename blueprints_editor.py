# ==================== КОНТЕКСТ КОМПИЛЯЦИИ ====================

@dataclass
class CompileContext:
    """
    Контекст компиляции цепочки выполнения.
    Хранит информацию о текущих отступах и доступных локальных переменных хука.
    """
    variables: dict = field(default_factory=dict)
    indent: str = '    '

    def indented(self, extra: str = '    ') -> 'CompileContext':
        return CompileContext(variables=self.variables, indent=self.indent + extra)

    def get(self, name: str, fallback: str = 'nil') -> str:
        return self.variables.get(name, fallback)


# ==================== КОМПИЛЯТОР ГРАФА ====================

class BlueprintCompiler:
    def __init__(self, graph):
        self.graph = graph
        self.visited_nodes = set()

    def compile(self) -> str:
        self.visited_nodes.clear()
        
        # Шаг 1: Сканируем граф для поиска всех уникальных названий переменных
        detected_variables = set()
        for node in self.graph.all_nodes():
            # Если нода имеет свойство 'var_name', значит это Set или Get Variable
            if node.get_property('var_name') is not None:
                name = node.get_property('var_name').strip().replace(' ', '_')
                if name:
                    detected_variables.add(name)

        # Шаг 2: Формируем заголовок файла
        lua = '-- Generated by WireBranch Blueprints Editor\n\n'
        
        if detected_variables:
            lua += '-- File-Scoped Script Variables\n'
            for var in sorted(detected_variables):
                lua += f'local {var} = nil\n'
            lua += '\n'

        # Шаг 3: Генерируем хуки
        for node in self.graph.all_nodes():
            if isinstance(node, nodes_gmod.BaseEventNode):
                lua += node.compile_event(self)
        return lua

    def resolve_port_value(self, port, ctx: CompileContext, fallback: str = '0') -> str:
        if not port:
            return fallback
        connections = port.connected_ports()
        if not connections:
            return fallback
            
        source_port = connections[0]
        source_node = source_port.node()
        
        node_id = id(source_node)
        if node_id in self.visited_nodes:
            return fallback
            
        self.visited_nodes.add(node_id)
        try:
            val = source_node.resolve_port_output_value(source_port.name(), self, ctx)
        finally:
            self.visited_nodes.remove(node_id)
            
        return val

    def compile_exec_chain(self, start_port, ctx: CompileContext) -> str:
        if not start_port:
            return ""
        connections = start_port.connected_ports()
        if not connections:
            return ""
            
        target_port = connections[0]
        target_node = target_port.node()
        
        if hasattr(target_node, 'compile_node'):
            return target_node.compile_node(self, ctx)
        return ""


